package com.ylink.fullgoal.fg;

public class RuleFg {

	private String ruleNO;//规则编号
	private String ruleName;//规则名称
	private String ruleRemark;//规则描述
	private String isPerposition;//前置后置

	public String getRuleNO() {
		return ruleNO;
	}
	public void setRuleNO(String ruleNO) {
		this.ruleNO = ruleNO;
	}
	public String getRuleName() {
		return ruleName;
	}
	public void setRuleName(String ruleName) {
		this.ruleName = ruleName;
	}
	public String getRuleRemark() {
		return ruleRemark;
	}
	public void setRuleRemark(String ruleRemark) {
		this.ruleRemark = ruleRemark;
	}
	public String getIsPerposition() {
		return isPerposition;
	}
	public void setIsPerposition(String isPerposition) {
		this.isPerposition = isPerposition;
	}
	
}
